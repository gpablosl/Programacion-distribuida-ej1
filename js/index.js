function calcularSuma(){

    n1 = $("#txtN1").val();
    n2 = $("#txtN2").val();

    res = parseInt(n1) + parseInt(n2)

    $("#txtRes").val(res)

 }

 function calcularResta(){
    n1 = $("#txtN1").val();
    n2 = $("#txtN2").val();

    res = parseInt(n1) - parseInt(n2)

    $("#txtRes").val(res)

 }